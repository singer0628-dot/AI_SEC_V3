### 5. 시계열 데이터 생성( 타임 스텝별 )
def create_rnn_data(raw_datas):
  global time_step
  x_datas = []
  y_datas = []
  for i in range(len(raw_datas)-time_step):
    x_datas.append(raw_datas[i:i+time_step])
    y_datas.append(raw_datas[i+time_step])

  return np.array(x_datas),np.array(y_datas)

### 6. 모델의 구성
###   - 과대적합 그래프로 모델의 파라미터를 줄이고 dropout을 50% 이상 증진함

from tensorflow.keras import Sequential,Input
from tensorflow.keras.layers import Dense,LSTM,Dropout,BatchNormalization
def train_model():
  global time_step;global target_list
  model = Sequential()
  model.add(Input((time_step,len(target_list))))
  lstm_1 = tf.keras.layers.LSTM(
        16,
        activation='tanh',
        recurrent_activation='sigmoid',
        return_sequences=True)
  lstm_2 = tf.keras.layers.LSTM(
        8,
        activation='tanh',
        recurrent_activation='sigmoid',
        return_sequences=False)
  model.add(lstm_1)
  model.add(lstm_2)
  model.add(Dense(64,activation="relu"))
  #model.add(BatchNormalization()) 과대적합의 원인
  model.add(Dropout(0.3))
  model.add(Dense(32,activation="relu"))
  model.add(Dropout(0.3))
  model.add(Dense(len(target_list),activation="linear"))
  model.compile(loss="mse",optimizer=tf.keras.optimizers.Adam(1.2e-3),metrics=["mae"])
  return model
#L2 규제 - 가중치 제한 하는 기법
