### 7. 훈련실행
def runn_train(model,x_train,y_train,epochs):
  early_obj = tf.keras.callbacks.EarlyStopping(
    monitor='val_loss',# 모니터링할 수치
    min_delta=0,# 떨어지는 값의 차이값 기준
    patience=10,# 최소수치에서 기다리는 횟수
    verbose=1,
    mode='auto',#자동 loss 더 낮은값을 체크하고 acc 더 높은 값을 체크
    restore_best_weights=False,# 조기종료시 최적의 값의 가중치로 복원
    start_from_epoch=0 #조기종료 체크 시점
)

  return model.fit(x_train,y_train,epochs=epochs,\
                   shuffle=False,\
                   validation_data=(x_train,y_train),\
                   batch_size=len(x_train)//5,callbacks=[early_obj])

### 8. 훈련결과 시각화 그래프
def rander_graph(res):
  print("xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx")
  print(res.history.keys())
  direction_acc = np.mean(
    np.sign(np.diff(res.history["loss"][-200:])) == np.sign(np.diff(res.history["val_loss"][-200:]))
  )
  print("방향성 정확도:",direction_acc)
  #dict_keys(['loss', 'mae', 'val_loss', 'val_mae'])
  plt.subplot(2,2,1)
  plt.plot(res.history["loss"],label="train_loss")
  plt.plot(res.history["val_loss"],label="valid_loss")
  plt.legend()
  plt.title("LOSSES")
  plt.xlabel(f"direction_acc: {direction_acc*100:.4f}%")
  plt.subplot(2,2,2)
  plt.plot(res.history["mae"],label="train_mae")
  plt.plot(res.history["val_mae"],label="valid_mae")
  plt.legend()
  plt.title("MAE")
  if not os.path.exists(r"{}/chart".format(RPATH)):
    os.makedirs(r"{}/chart".format(RPATH))
  plt.savefig(r"{}/chart/{}_mse_mae.png".format(RPATH,coin_name))
  plt.show()

